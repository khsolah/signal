const get = (
  category?: string,
  product?: string
): Promise<IProductAbstract[] | IProduct> => {
  return new Promise((resolve, reject) => {
    if (product && category) {
      let result: IProduct[] = []
      switch (category) {
        case 'Keyboards':
          result = keyboardsList.filter(element => element.name === product)
          break
        case 'Keycaps':
          result = keycapsList.filter(element => element.name === product)
          break
        case 'Switches':
          result = switchesList.filter(element => element.name === product)
          break
        case 'DIY Kits':
          result = diyList.filter(element => element.name === product)
          break
        case 'featured':
          result = featuredList.filter(element => element.name === product)
          break
      }

      if (result.length === 0) {
        reject({
          status: 404,
          statusMessage: 'Not Found',
        })
      } else {
        resolve(result[0])
      }
    }

    if (category) {
      let data: IProductAbstract[] = []
      switch (category) {
        case 'Keyboards':
          resolve(keyboardsList)
        case 'Keycaps':
          resolve(keycapsList)
        case 'Switches':
          resolve(switchesList)
        case 'DIY Kits':
          resolve(diyList)
        default:
          resolve([...keycapsList, ...switchesList, ...diyList, ...keyboardsList])
      }
    }

    resolve([...keycapsList, ...switchesList, ...diyList, ...keyboardsList])
  })
}

export default {
  get,
}
